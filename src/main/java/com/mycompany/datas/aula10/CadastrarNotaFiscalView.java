    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        int numero = Integer.parseInt(txtNumero.getText());
        double valor = Double.parseDouble(txtValor.getText());
        Date data = jdcData.getDate();
        
        NotaFiscal obj = new NotaFiscal();
        obj.setNumeroNota(numero);
        obj.setValorNota(valor);
        obj.setDataNota(data);
        
        try {
            NotaFiscalDAO.salvar(obj);
        } catch (SQLException ex) {
            Logger.getLogger(CadastrarNotaFiscalView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

